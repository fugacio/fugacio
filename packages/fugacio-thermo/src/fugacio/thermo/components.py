def _with_supplements(c: Component) -> Component:
    """Backfill generated side-table data onto a curated component record.

    The inline records carry the hand-transcribed textbook constants; the
    generated tables supply everything else so the whole database is uniformly
    usable: ``gform_ig`` (``scripts/gen_formation.py``), dipole moments and
    Rackett ``Z_RA`` values, and Antoine / ideal-gas-``Cp`` fits for records
    that lack an inline one (``scripts/gen_property_data.py``). Inline values
    always win over backfills.
    """
    updates: dict[str, object] = {}
    if c.gform_ig is None and c.name in GFORM_IG:
        updates["gform_ig"] = GFORM_IG[c.name]
    if c.dipole is None and c.name in DIPOLE:
        updates["dipole"] = DIPOLE[c.name]
    if c.zra is None and c.name in ZRA:
        updates["zra"] = ZRA[c.name]
    if c.antoine is None and c.name in ANTOINE_BACKFILL:
        a, b, cc, t_min, t_max = ANTOINE_BACKFILL[c.name]
        updates["antoine"] = AntoineCoeffs(a=a, b=b, c=cc, t_min=t_min, t_max=t_max)
    if c.cp_ig is None and c.name in CP_BACKFILL:
        a, b, cc, e, t_min, t_max = CP_BACKFILL[c.name]
        updates["cp_ig"] = CpIdeal(a=a, b=b, c=cc, d=0.0, e=e, t_min=t_min, t_max=t_max)
    return replace(c, **updates) if updates else c  # type: ignore[arg-type]


#: Canonical-name -> `Component` lookup for the curated database.
DATABASE: dict[str, Component] = {c.name: _with_supplements(c) for c in _COMPONENTS}


def get(name: str) -> Component:
    """Return the `Component` for ``name`` (case-insensitive).

    Raises:
        KeyError: if ``name`` is not present in `DATABASE`.
    """
    key = name.strip().lower()
    if key not in DATABASE:
        raise KeyError(f"unknown component {name!r}; known components: {sorted(DATABASE)}")
    return DATABASE[key]


def names() -> list[str]:
    """Return the sorted list of component names in the database."""
    return sorted(DATABASE)


def component_arrays(components: list[str] | list[Component]) -> dict[str, Array]:
    """Stack the core EOS constants of several components into JAX arrays.

    Args:
        components: A list of component names or `Component` objects.

    Returns:
        A dict with keys ``"tc"``, ``"pc"``, ``"omega"`` and ``"mw"`` mapping to
        1-D arrays aligned with the input order, ready to feed the equation-of-
        state kernels.
    """
    resolved = [get(c) if isinstance(c, str) else c for c in components]
    return {
        "tc": jnp.asarray([c.tc for c in resolved]),
        "pc": jnp.asarray([c.pc for c in resolved]),
        "omega": jnp.asarray([c.omega for c in resolved]),
        "mw": jnp.asarray([c.mw for c in resolved]),
    }

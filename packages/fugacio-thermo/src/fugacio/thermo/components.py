def _with_formation(c: Component) -> Component:
    """Backfill a bulk ``gform_ig`` onto a component whose record omits it.

    The textbook subset defines ``gform_ig`` inline; the remainder is filled from
    the generated :data:`GFORM_IG` table so reaction thermochemistry is available
    database-wide (see ``scripts/gen_formation.py``).
    """
    if c.gform_ig is None and c.name in GFORM_IG:
        return replace(c, gform_ig=GFORM_IG[c.name])
    return c


#: Canonical-name -> :class:`Component` lookup for the curated database.
DATABASE: dict[str, Component] = {c.name: _with_formation(c) for c in _COMPONENTS}


def get(name: str) -> Component:
    """Return the :class:`Component` for ``name`` (case-insensitive).

    Raises:
        KeyError: if ``name`` is not present in :data:`DATABASE`.
    """
    key = name.strip().lower()
    if key not in DATABASE:
        raise KeyError(f"unknown component {name!r}; known components: {sorted(DATABASE)}")
    return DATABASE[key]


def names() -> list[str]:
    """Return the sorted list of component names in the database."""
    return sorted(DATABASE)


